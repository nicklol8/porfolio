const pokestopModal = () => {
  const pokestopModal = document.getElementById('pokestop-modal');
  if (pokestopModal.style.display === 'block') {
    pokestopModal.style.display = 'none';
  } else {
    pokestopModal.style.display = 'block';
  }
};
